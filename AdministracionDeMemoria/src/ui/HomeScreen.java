    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        //Metodo para realizar el guarado de los contactos agregados
        contact = new Contact(); //objeto
        contact.setContactId(txtId.getText());
        contact.setName(txtName.getText());
        contact.setLastName(txtLastName.getText());
        contact.setBirthDay(txtBirthDay.getText());
        contact.setAge(Byte.parseByte(txtAge.getText()));
        contact.setAddress(txtAddress.getText());
        contact.setPhoneNumber(txtPhoneNumber.getText());
        contact.setCategory(txtCategory.getText());
        
        contactDAO = new contactDAO();
        if(contactDAO.saveContact(contact)){
            JOptionPane.showMessageDialog(this, "Conctacto guardado con exito");
            
        }else{
            JOptionPane.showMessageDialog(this, "Servicio no disponible");
        }
        
    }//GEN-LAST:event_btnSave1ActionPerformed

